  editor="${EDITOR:-}"
  if [[ -z "$editor" ]]; then
    if has_cmd nano; then editor="nano"; elif has_cmd vim; then editor="vim"; elif has_cmd vi; then editor="vi"; fi
  fi
  if [[ -n "$editor" ]]; then
    "$editor" "$file"
  else
    warn "未找到 nano/vim/vi。改用覆盖粘贴模式。"
