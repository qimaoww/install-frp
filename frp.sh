    for svc in "${restart_services[@]}"; do
      if systemctl list-unit-files "${svc}.service" >/dev/null 2>&1; then
        systemctl restart "$svc" || warn "重启 ${svc} 失败，请查看 systemd 状态。"
      fi
    done
  else
    warn "未重启服务，新的 log.to 要等下次重启后生效。"
  fi
}

