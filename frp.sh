  sed -E \
    -e 's#(auth\.token[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(token[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(secretKey[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(webServer\.password[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(httpPassword[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(password[[:space:]]*=[[:space:]]*").*(")#\1******\2#g' \
    -e 's#(GH_PROXY=).+#\1******#g'
}

