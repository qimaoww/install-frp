    cat >> "$tmp" <<'EOF_MINIMAL_PROXY'
[[proxies]]
name = "${name}"
type = "tcp"
localIP = "${localIP}"
localPort = ${localPort}
remotePort = ${remotePort}
EOF_MINIMAL_PROXY
  else
    paste_until_eof_to_file "${tmp}.body"
    cat "${tmp}.body" >> "$tmp"
    rm -f "${tmp}.body"
  fi

  install -m 0640 "$tmp" "$file"
  rm -f "$tmp"
  chown root:"$FRP_USER" "$file" 2>/dev/null || true
  ok "已创建预设：$file"
  echo "以后可在菜单中选择该预设，并按变量交互生成 frpc.d/*.toml。"
}

